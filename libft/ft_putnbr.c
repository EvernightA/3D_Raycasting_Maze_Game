#include "ft_printf.h"

void	ft_putnbr(int nb)
{
	int	r;

	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		ft_putchar('-');
		nb *= -1;
	}
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
	}
	r = nb % 10 + '0';
	write(1, &r, 1);
}
