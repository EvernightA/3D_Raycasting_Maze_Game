#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	r;

	if (n == -2147483648)
	{
		ft_putstr_fd("-2147483648", fd);
		return ;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n *= -1;
	}
	if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
	}
	r = n % 10 + '0';
	write (fd, &r, 1);
}
